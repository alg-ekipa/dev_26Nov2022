# Naredba samo IF

a = 5

if a < 10:
    print('Broj {a} je manji od 10')
print('Sada smo izvan if petlje')
print()

# Nareedba IF-ELSE

b = 19

if b < 15:
    print(f'Broj {b} je manji of 15')
    print('Sada smo u bloku IF')
else:
    print('Broj {b} nije manji od 15')
    print('Sada smo u bloku ELSE')
print('Sada smo izvan bloka IF-ELSE')
print()

