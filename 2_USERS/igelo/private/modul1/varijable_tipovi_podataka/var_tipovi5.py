
broj1 = int(input('Unesi prvi broj:'))
broj2 = int(input('Unesi drugi broj:'))

zbroj = broj1 + broj2
print (f'{broj1} + {broj2} = {zbroj}')

oduzimanje = broj1 - broj2
print (f'{broj1} - {broj2} = {oduzimanje}')

umnožak = broj1 * broj2
print (f'{broj1} * {broj2} = {umnožak}')

dijeljenje = broj1 / broj2
print (f'{broj1} / {broj2} = {dijeljenje}')

