'''
izračun mjesečne potrošnje električne energije
snaga je 1.3 kW
potrošnja je 2 sata
cijena 1 kW je 1 kn
'''

snaga_pecnice = 1.3
potrosnja = 2
cijena_potrosnje = 1

ukupna_potrosnja = snaga_pecnice * potrosnja * cijena_potrosnje

print ('Ukupna potrošnja pečnice je ', ukupna_potrosnja, 'kW')
print ('Ukupna cijena potrošnje je', ukupna_potrosnja, 'kn')

